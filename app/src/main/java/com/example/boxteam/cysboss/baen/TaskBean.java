package com.example.boxteam.cysboss.baen;

public class TaskBean {
    private int img;
    private String text;
    private String tv;

    public TaskBean() {
        this.img = img;
        this.text = text;
        this.tv = tv;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getTv() {
        return tv;
    }

    public void setTv(String tv) {
        this.tv = tv;
    }
}
